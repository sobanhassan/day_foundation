        <details>
            <summary><strong>What kinds of grant applications does the Doris &amp; Victor  Day Foundation prefer not to review?</strong></summary>
          <p>The  Board of Directors generally does not consider requests of annual fund-raising  drives, services which are commonly recognized as government or school  obligations, debt reduction, endowments, religious programs that advocate  specific religious doctrines or do not serve the broader community, or grants  to individuals (other than scholarships).</p>
        </details><br />
        <details>
            <summary><strong>How  does the does the Doris &amp; Victor Day Foundation decide what to fund?</strong></summary>
          <p>Our committees make decisions based on our efforts to:</p>
            <ul>
              <li><strong>Invest wisely</strong>: Consider the strength of  the organization and the potential benefits to the Quad Cities residents; the  Doris &amp; Victor Day Foundation may fully fund, partially fund or decline to  fund a request</li>
              <li><strong>Prioritize: </strong>Determine which programs meet  real needs in the community and how Doris &amp; Victor Day Foundation funding  can be most valuable</li>
              <li><strong>Be Diligent:</strong> Require clear plans for  implementation and evaluation of the program or project</li>
              <li><strong>Think Long-Term</strong>: Consider the  sustainability of the project</li>
              <li><strong>Consider Other Funding</strong>: Encourage diverse  funding, including the organization’s own</li>
              <li><strong>Think Bigger</strong>: When possible, we hope grants can have more  transformational impact on your organization or the community </li>
            </ul>
        </details><br />
        <details>
            <summary><strong>What  makes a strong grant application?</strong></summary>
            <ul>
              <li>The narrative is written clearly and succinctly</li>
              <li>The application explains what the program or  project hopes to accomplish, why it is important, and how that outcome will  benefit community members</li>
              <li>Board of Directors will understand how you will  achieve the results you propose in the application</li>
              <li>Based on the explanation of the budget and other  funding, Board of Directors will know your priorities and how the project will  change depending on changes in funding </li>
            </ul>
